# Ejercicio 1-1. explique lo que sucede con
#
#	$ date\@
#

date\@

# Shell interpreta que estamos pasando el comando 'date@' porque la contrabarra '\' escapa el carácter '@' evitando que éste desempeñe las funciones que en algunos shell desempeña ese símbolo. Nótese que en los shell actuales no se utiliza el simbolo '@' para la interrupción de las líneas.